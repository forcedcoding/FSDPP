// Theme toggle function
const themeToggle = document.querySelector(".theme-toggle");
themeToggle.addEventListener("click", () => {
    document.body.classList.toggle("dark-mode");
    document.querySelector(".head").classList.toggle("dark-mode");
    document.querySelector(".sidebar").classList.toggle("dark-mode");
    
    document.querySelectorAll(".card").forEach(card => card.classList.toggle("dark-mode"));

    themeToggle.textContent = document.body.classList.contains("dark-mode") ? "Light Mode" : "Dark Mode";
});

// Video rendering function
const container = document.getElementById("videoContainer");

function renderVideos(videos) {
    container.innerHTML = "";
    videos.forEach(video => {
        const videoCard = document.createElement("div");
        videoCard.classList.add("card");
        videoCard.style.maxHeight = "190px"
        videoCard.innerHTML = `
            <a href="${video.url}">
                <img src="${video.thumbnail}" alt="${video.title}">
            </a>
            <div class="card-content">
                <h3>${video.title}</h3>
            </div>
        `;
        container.appendChild(videoCard);
    });
}

// Search functionality
const searchInput = document.getElementById("search");

function searchVideos() {
    const searchText = searchInput.value.toLowerCase();
    const filteredVideos = data.videos.filter(video =>
        video.title.toLowerCase().includes(searchText)
    );
    renderVideos(filteredVideos);
}

searchInput.addEventListener("input", searchVideos);

// Load videos when the page loads
document.addEventListener("DOMContentLoaded", () => renderVideos(data.videos));
